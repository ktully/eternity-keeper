var CloneFactory = {};
CloneFactory.clone = function (element) {
	if (element == null || !element.clone) {
		console.error('Tried to clone something which was not an HTML element.');
		return;
	}

	var spawn = element.clone();
	spawn.attr('id', null);
	spawn.data('bound', null);
	
	return spawn;
};
